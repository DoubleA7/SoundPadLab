				<p>Many people with visual impairment actively play soccer, however the task of making the game
					accessible is met with significant challenges. These challenges include: the need to constantly
					talk to signify location and detecting the positions of silent objects on the field. Our work aims
					to discover methods to help persons with visual impairments play soccer more efficiently and safely.
					The proposed system uses headphone-rendered spatial audio, an on-person computer, and sensors to
					create 3D sound that represents the objects on the field in real-time. This depiction of the
					field will help players to more accurately detect the locations of objects and people on the field.
					The present work describes the design of such a system and discusses perceptual challenges. Broadly,
					our work aims to discover ways to enable people with visual impairments to detect the position of
					moving objects, which will allow them to feel empowered in their personal lives and give them the
					confidence to navigate more independently.</p>
